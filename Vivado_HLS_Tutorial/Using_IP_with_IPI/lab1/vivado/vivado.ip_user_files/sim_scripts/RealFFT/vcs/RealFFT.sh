# Create design library directory paths and define design library mappings in cds.lib
create_lib_mappings()
{
  libs=(xil_defaultlib xbip_utils_v3_0_4 axi_utils_v2_0_0 c_reg_fd_v12_0_0 xbip_dsp48_wrapper_v3_0_4 xbip_pipe_v3_0_0 xbip_dsp48_addsub_v3_0_0 xbip_addsub_v3_0_0 c_addsub_v12_0_7 c_mux_bit_v12_0_0 c_shift_ram_v12_0_7 xbip_bram18k_v3_0_0 mult_gen_v12_0_9 cmpy_v6_0_9 floating_point_v7_0_10 xfft_v9_0_8)
  file="synopsys_sim.setup"
  dir="vcs"

  if [[ -e $file ]]; then
    rm -f $file
  fi

  if [[ -e $dir ]]; then
    rm -rf $dir
  fi

  touch $file
  lib_map_path="<SPECIFY_COMPILED_LIB_PATH>"
  if [[ ($1 != "" && -e $1) ]]; then
    lib_map_path="$1"
  else
    echo -e "ERROR: Compiled simulation library directory path not specified or does not exist (type "./top.sh -help" for more information)\n"
  fi
  incl_ref="OTHERS=$lib_map_path/synopsys_sim.setup"
  echo $incl_ref >> $file

  for (( i=0; i<${#libs[*]}; i++ )); do
    lib="${libs[i]}"
    lib_dir="$dir/$lib"
    if [[ ! -e $lib_dir ]]; then
      mkdir -p $lib_dir
      mapping="$lib : $dir/$lib"
      echo $mapping >> $file
    fi
  done
}


